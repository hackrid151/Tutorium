package Aufgabe2;

public class Perle {
    private Perle nachfolger;
    private String farbe;

    Perle(String farbe){
        this.farbe = farbe;
    }

    public void setNachfolger(){
        return nachfolger;
    }

    public void getNachfolger() {
        return nachfolger;
    }

    public String getFarbe() {
        return farbe;
    }
}
